void PrintArrayMatrix(int[,] matr)
{
    for (int i = 0; i < matr.GetLength(0); i++) // matrix.GetLength(0) получаем длину из 0 позиции инициализированного массива (3)
    {
        for (int j = 0; j < matr.GetLength(1); j++)
        {
            Console.Write($"{matr[i, j]} ");
        }
        Console.WriteLine(); // перевод строки после того как пробежались по строке i и вывели все элементы этой строки.
    }
}

