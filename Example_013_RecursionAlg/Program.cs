double Factorial(double num) //тип дабл, т.к. факториал очень быстрорастущая функция и быстро вывзывает переполнение типа int
{
    // 1! = 1
    // 0! = 1
    if (num == 1 || num == 0) return 1;
    else return num *= Factorial(num - 1);
}

